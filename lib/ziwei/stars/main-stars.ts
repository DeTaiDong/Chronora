// ─── 14主星安法 ────────────────────────────────────────────────────────────
//
// 紫微系（6星）：紫微为基准，逆向偏移（存在隔宫）
//   紫微:+0  天机:-1  [隔]  太阳:-3  武曲:-4  天同:-5  [隔×3]  廉贞:-9
//
// 天府系（8星）：天府位 = (18 - 紫微位) % 12，再顺数
//   天府:+0  太阴:+1  贪狼:+2  巨门:+3  天相:+4  天梁:+5
//   七杀:+6  [隔×3]  破军:+10
//
// 各宫可同时容纳多颗主星。

export function placeMainStars(ziWeiIdx: number): Record<string, number> {
  const zw = ziWeiIdx;
  const tf = (18 - zw + 12) % 12; // 天府位

  return {
    // 紫微系
    "紫微": zw,
    "天机": (zw - 1 + 12) % 12,
    "太阳": (zw - 3 + 12) % 12,
    "武曲": (zw - 4 + 12) % 12,
    "天同": (zw - 5 + 12) % 12,
    "廉贞": (zw - 9 + 12) % 12,
    // 天府系
    "天府": tf,
    "太阴": (tf + 1)  % 12,
    "贪狼": (tf + 2)  % 12,
    "巨门": (tf + 3)  % 12,
    "天相": (tf + 4)  % 12,
    "天梁": (tf + 5)  % 12,
    "七杀": (tf + 6)  % 12,
    "破军": (tf + 10) % 12,
  };
}
